import matplotlib.pyplot as plt

# Valores del eje x
x = [1, 2, 3]

# Los valores correspondientes del eje y
y = [2, 6, 3, 6, 3]

# Trazando los puntos 
plt.plot(x, y)

# Nombrando el eje X
plt.xlabel('x – eje x')

# Nombrando el eje Y
plt.ylabel('y – eje y')

# Dando un título a mi gráfico
plt.title('Mi grafico xde')

# Función para mostrar la trama
plt.show()
